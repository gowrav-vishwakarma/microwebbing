<h2>mw.tools</h2>
  <ul>
    <li><span>.isEmptyObject(<strong>Object</strong>)</span>
      <p>checks if <strong>Object</strong> has any properties</p>
    </li>
    <li><span>.migrateAttributes(<strong>from</strong>, <strong>to</strong>, <strong>except</strong>)</span>
      <p>Copies attributes from one element to another. The first to parameters are required. The <strong>except</strong> attribute is an array with parameters that you don't want to copy</p>
    </li>
    <li><span>.getAttrs(<strong>node</strong>)</span>
      <p>Collects all attributes from a node in an Object<em> (ex: {title:'Some title'})</em></p>
    </li>
    <li><span>.get_filename(<strong>String</strong>)</span>
      <p>Returns file name from a given url string <em> (ex: example.com/image.jpg - returns 'image.jpg')</em></p>
    </li>
    <li><span>.fullscreen(<strong>node</strong>)</span>
      <p>Puts node element in fullscreen mode</p>
    </li>
  </ul>